// For Loop
//     Start  ;  Stop ; Step
for (let i = 1; i <= 5; i++) {
    console.log(i)
}

// While Loop
let counter = 1
while (counter <= 5) {
    console.log(counter)
    counter++
}

// Do While
let number = 5
do {
    number++
    console.log(number)
} while (number <= 10)

// Infinite Loop
counter = 0
while (true) {
    counter++
    console.log(counter, "Don't Repeat Yourself")
}
